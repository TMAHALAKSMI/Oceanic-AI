def run_tests(base_url, image_path):
    passed = failed = 0

    def check(name, fn):
        nonlocal passed, failed
        try:
            fn()
            print(f"  ✓  {name}")
            passed += 1
        except Exception as e:
            print(f"  ✗  {name}: {e}")
            failed += 1

    print(f"\nTesting: {base_url}\n")

    # 1. Health
    def t_health():
        d = get(f"{base_url}/health")
        assert d["status"] == "healthy"
    check("GET /health", t_health)

    # 2. Root
    def t_root():
        d = get(f"{base_url}/")
        assert "OceanWatch" in d.get("message","") or d.get("status") == "operational"
    check("GET /", t_root)

    # 3. Stats
    def t_stats():
        d = get(f"{base_url}/api/stats")
        assert "total_analyses" in d
    check("GET /api/stats", t_stats)

    # 4. History
    def t_history():
        d = get(f"{base_url}/api/history")
        assert "records" in d
    check("GET /api/history", t_history)

    # 5. Detection
    def t_detect():
        d = post_file(f"{base_url}/api/detect", image_path)
        assert "id" in d
        assert "result_url" in d
        assert isinstance(d["total_detections"], int)
        print(f"       → id={d['id']}  detections={d['total_detections']}  health={d['ocean_health_score']}")
    check("POST /api/detect", t_detect)

    print(f"\nResults: {passed} passed, {failed} failed\n")
    return failed == 0


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--url",   default="http://127.0.0.1:8000")
    ap.add_argument("--image", default=None)
    args = ap.parse_args()

    img = args.image or create_test_image()
    ok  = run_tests(args.url.rstrip("/"), img)
    sys.exit(0 if ok else 1)


if __name__ == "__main__":
    main()
